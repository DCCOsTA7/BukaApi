     public function addNewTopic(Request $request)
    {
        
        $topic = new Topic();
        $topic->name=$request['name'];
        $topic->description=$request['description'];
        $topic->save();

        return response()->json([
            'status_code'=>201,
            'message'=>'Topic created successfuly',
            'data' => $topic
            ]);
    }

